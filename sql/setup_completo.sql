  id bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  animal text NOT NULL,
  numero integer NOT NULL DEFAULT 0,
  hora text NOT NULL,
  fecha date NOT NULL,
  emoji text NOT NULL DEFAULT '🐾',
