CREATE OR REPLACE FUNCTION public.verify_admin_login(admin_email text, admin_password text)
RETURNS boolean
LANGUAGE plpgsql
SECURITY DEFINER
AS $$
DECLARE
  is_valid boolean;
BEGIN
  SELECT EXISTS (
    SELECT 1
    FROM public.admins
    WHERE email = admin_email
    AND password_hash = crypt(admin_password, password_hash)
  ) INTO is_valid;

  RETURN coalesce(is_valid, false);
END;
$$;

