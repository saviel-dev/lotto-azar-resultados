LANGUAGE plpgsql
SECURITY DEFINER
AS $$
BEGIN
  UPDATE public.sorteos
  SET animal = p_animal,
      numero = p_numero,
      hora = p_hora,
      fecha = p_fecha,
      emoji = p_emoji
  WHERE id = p_id;

  RETURN FOUND;
END;
$$;

CREATE OR REPLACE FUNCTION public.delete_sorteo(p_id bigint)
RETURNS boolean
LANGUAGE plpgsql
SECURITY DEFINER
AS $$
BEGIN
  DELETE FROM public.sorteos WHERE id = p_id;
  RETURN FOUND;
END;
$$;

